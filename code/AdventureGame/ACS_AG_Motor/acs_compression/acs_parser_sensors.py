import json
import os
from acs_compression import (
    build_shared_dictionary, emit_compressed_field_asm, compute_stats,
    SENSOR_STRING_FIELDS as STRING_FIELDS
)

JSON_FILE = "acs_sensors.json"
ASM_FILE  = "acs_sensors.asm"


def bool_to_byte(value):
    if isinstance(value, bool):
        return 1 if value else 0
    if isinstance(value, str):
        return 1 if value.lower() in ("true", "1", "yes") else 0
    return 1 if value else 0


def ascii_lines(text):
    segments = text.split("\n")
    out = []
    for si, seg in enumerate(segments):
        if si < len(segments) - 1:
            seg += "\\n"
        while len(seg) > 80:
            out.append(f'  .ascii "{seg[:80]}"')
            seg = seg[80:]
        if seg:
            out.append(f'  .ascii "{seg}"')
    return out


def to_asm(sensors):
    word_to_id, id_to_word, freq = build_shared_dictionary()

    lines = []
    lines.append("; ============================================================")
    lines.append(";  ACS Sensors — auto-generated by acs_parser_sensors.py")
    lines.append(";  Uses shared token dictionary from acs_screens.asm")
    lines.append("; ============================================================")
    lines.append("")

    lines.append("sensors_index:")
    for index, (key, sen) in enumerate(sensors.items()):
        sen_id = sen.get("ID", str(index)).strip()
        name   = sen.get("Name", key)
        lines.append(f"  .word sensor_pointer_{sen_id}  ; {name}")
    lines.append("sensors_index_record_length:")
    lines.append("  .byte 2")
    lines.append("")

    lines.append("sensors_pointers:")
    offset = 0
    sen0_offsets = {}
    sen0_record_length = None

    pointer_fields = [
        "id", "name", "active", "dialog_on", "dialog_off", "toggle",
        "dialog_on_compressed", "dialog_off_compressed",
    ]

    for index, (key, sen) in enumerate(sensors.items()):
        sen_id = sen.get("ID", str(index)).strip()
        label  = f"sensor_{sen_id}"
        name   = sen.get("Name", key)
        start_offset = offset

        lines.append(f"sensor_pointer_{sen_id}:")
        for fname in pointer_fields:
            pad = " " * max(0, 30 - len(fname))
            lines.append(f"  .word {label}_{fname}{pad}; {name} {fname} [{offset},{offset+1}]")
            if index == 0:
                sen0_offsets[fname] = offset
            offset += 2

        if index == 0:
            sen0_record_length = offset - start_offset

    for fname in pointer_fields[1:]:
        lines.append(f"sensor_{fname}_offset:")
        lines.append(f"  .byte {sen0_offsets[fname]}")
    lines.append(f"sensor_record_length:")
    lines.append(f"  .byte {sen0_record_length}")
    lines.append("")

    for index, (key, sen) in enumerate(sensors.items()):
        sen_id   = sen.get("ID", str(index)).strip()
        label    = f"sensor_{sen_id}"
        name_str = sen.get("Name", "").replace('"', '\\"')
        active   = bool_to_byte(sen.get("Active", False))
        dialog_on_str  = sen.get("DialogOn",  "").replace('"', '\\"')
        dialog_off_str = sen.get("DialogOff", "").replace('"', '\\"')
        dialog_on_raw  = sen.get("DialogOn",  "")
        dialog_off_raw = sen.get("DialogOff", "")

        lines.append(f"; ── Sensor {sen_id}: {sen.get('Name', key)} ──────────────────────────")

        lines.append(f"{label}_id:")
        lines.append(f"  .byte {sen_id}")
        lines.append("")

        lines.append(f"{label}_name:")
        lines.extend(ascii_lines(name_str))
        lines.append("")

        lines.append(f"{label}_active:")
        lines.append(f"  .byte {active}  ; {'on' if active else 'off'}")
        lines.append("")

        # DialogOn (commented out original)
        lines.append(f"{label}_dialog_on:")
        for aline in ascii_lines(dialog_on_str):
            lines.append(f"  ;{aline.lstrip()}")
        lines.append("")

        lines.extend(emit_compressed_field_asm(
            f"{label}_dialog_on_compressed", dialog_on_raw, word_to_id, id_to_word))

        # DialogOff (commented out original)
        lines.append(f"{label}_dialog_off:")
        for aline in ascii_lines(dialog_off_str):
            lines.append(f"  ;{aline.lstrip()}")
        lines.append("")

        lines.extend(emit_compressed_field_asm(
            f"{label}_dialog_off_compressed", dialog_off_raw, word_to_id, id_to_word))

        try:
            toggle_val = 1 if int(sen.get("Toggle", 0)) else 0
        except (ValueError, TypeError):
            toggle_val = 0
        lines.append(f"{label}_toggle:")
        lines.append(f"  .byte {toggle_val}  ; {'toggle' if toggle_val else 'normal'}")
        lines.append("")

    lines.append("sensor_count:")
    lines.append(f"  .byte {len(sensors)}")
    lines.append("")

    texts = [sen.get(f, "") for sen in sensors.values() for f in STRING_FIELDS if sen.get(f, "").strip()]
    total_orig, total_comp, dict_bytes = compute_stats(texts, word_to_id)
    net = total_comp + dict_bytes
    savings = total_orig - net
    pct = (savings / total_orig * 100) if total_orig > 0 else 0
    lines.append("; ── Compression stats (sensors only, shared dict) ──────────")
    lines.append(f";  Sensor original string bytes: {total_orig}")
    lines.append(f";  Sensor compressed bytes (1-byte IDs): {total_comp}")
    lines.append(f";  Dictionary overhead (shared, paid once): {dict_bytes}")
    lines.append(f";  Net: {net} vs {total_orig}")
    lines.append(f";  Savings: {savings} bytes ({pct:.1f}%)")
    lines.append("")

    return "\n".join(lines)


def main():
    if not os.path.exists(JSON_FILE):
        print(f"[ERROR] '{JSON_FILE}' not found.")
        return
    with open(JSON_FILE, "r", encoding="utf-8") as f:
        sensors = json.load(f)
    if not sensors:
        print("[WARNING] No sensors found.")
        return
    sensors = dict(sorted(sensors.items(), key=lambda item: int(item[1].get("ID", 0))))
    asm = to_asm(sensors)
    with open(ASM_FILE, "w", encoding="utf-8") as f:
        f.write(asm)
    print(f"[OK] Generated '{ASM_FILE}' with {len(sensors)} sensor(s).")


if __name__ == "__main__":
    main()
